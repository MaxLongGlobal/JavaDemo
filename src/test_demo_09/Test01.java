package test_demo_09;

public class Test01 {
	//	简单示例程序演示了算术运算符
	public static void main(String[] args) {
		
		int a = 100;
		int b = 20;
//		int c = 30;
		int d = 50;
		
		System.out.println("a + b = " + (a + b));
		System.out.println("a - b = " + (a - b));
		System.out.println("a * b = " + (a * b));
		System.out.println("a / b = " + (a / b));
		System.out.println("a % b = " + (a % b));
		System.out.println("a++ = " + (a++));
		System.out.println("a-- = " + (a--));

		System.out.println("d++ = " + (d++));
		System.out.println("++d = " + (++d));



	}

}
